    private void atualizarEstilo() {
        if (selecionado) {
            setBackground(Color.CYAN);
            setBorder(BorderFactory.createLineBorder(Color.BLUE, 2));
        } else {
            setBackground(Color.LIGHT_GRAY);
            setBorder(BorderFactory.createLineBorder(Color.GRAY));
        }
        repaint();
    }

