    public CardCartao(Cartao cartao, Compra compra) {
        initComponents();
        
        nomeMetodoField.setText(cartao.getNomeMeioPagamento());
        nomeTitularField.setText(cartao.getNomeTitular());
        numeroField.setText(cartao.getNumero());
        cvvField.setText(cartao.getCvv());
        setBackground(Color.LIGHT_GRAY); // cor padrão
        setBorder(BorderFactory.createLineBorder(Color.GRAY));

        addMouseListener(new MouseAdapter() {
            @Override
            public void mouseClicked(MouseEvent e) {
                selecionado = true;
                atualizarEstilo();
                int resposta = javax.swing.JOptionPane.showConfirmDialog(CardCartao.this,
                    "Deseja realmente pagar com este cartão?",
                    "Confirmar pagamento",
                    javax.swing.JOptionPane.YES_NO_OPTION
                );

                if (resposta == javax.swing.JOptionPane.YES_OPTION) {
                    try {
                        CompraController.editarStatusCompra(compra);
                        Pagamento p = new Pagamento(null, compra.getVendedor(), compra, cartao, LocalDateTime.now());
                        PagamentoController.inserirPagamento(p);
                    } catch (IOException ex) {
                        Logger.getLogger(CardCartao.class.getName()).log(Level.SEVERE, null, ex);
                    }
                    javax.swing.JOptionPane.showMessageDialog(CardCartao.this, "Pagamento efetuado com sucesso.");
                    java.awt.Component c = CardCartao.this;
                    while (c != null && !(c instanceof javax.swing.JDialog)) {
                        c = c.getParent();
                    }
                    if (c instanceof javax.swing.JDialog dialog) {
                        dialog.dispose();
                    }
                }
                selecionado = false;
                atualizarEstilo();
            }
        });
    }

