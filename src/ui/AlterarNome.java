    private void confirmarButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmarButtonActionPerformed
        // TODO add your handling code here:
        String nome = nomeField.getText();
       
        if(nome.isEmpty()){
            javax.swing.JOptionPane.showMessageDialog(this, "Preencha o campo.");
            return;
        }
        
        if(!UsuarioController.nomeValido(nome)){
            javax.swing.JOptionPane.showMessageDialog(this, "Nome inv√°lido.");
            return;
        }
        
        Usuario user = Sistema.getInstance().getUsuarioLogado();
        UsuarioController uc = new UsuarioController();
        
        try {
            uc.alterarNome(nome, user);
            javax.swing.JOptionPane.showMessageDialog(this, "Nome alterado com sucesso.");
        } catch (IOException ex) {
            Logger.getLogger(AlterarNome.class.getName()).log(Level.SEVERE, null, ex);
        }
        this.dispose();
    }//GEN-LAST:event_confirmarButtonActionPerformed

